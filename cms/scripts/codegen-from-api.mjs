// CMS Codegen via Worker API: Fetches published content from the public REST API
// Uses curl for HTTP (works around Node.js fetch connectivity issues with workers.dev).
// No Supabase secrets needed.
// Usage: node codegen-from-api.mjs
//   API_URL=https://puxijie-cms-api.hzjeric2002.workers.dev node codegen-from-api.mjs

import { execSync } from 'child_process';
import { writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';

const API_BASE = process.env.API_URL || 'https://puxijie-cms-api.hzjeric2002.workers.dev';
const OUTPUT_DIR = join(process.cwd(), 'src');

const CMS_CATEGORY_MAP = {
  waterproof_bt: 'Waterproof Bluetooth Speaker',
  normal_bt: 'Normal Bluetooth Speaker',
  specialty: 'Specialty Speaker',
  earbuds: 'Bluetooth Earbuds',
};

function curl(url) {
  const stdout = execSync(`curl -sS --connect-timeout 15 --max-time 30 "${url}"`, {
    encoding: 'utf-8',
    stdio: ['pipe', 'pipe', 'pipe'],
  });
  const json = JSON.parse(stdout);
  if (!json.success) throw new Error(`API error: ${json.error || 'unknown'}`);
  return json.data;
}

async function fetchAll(path, limit = 100) {
  const separator = path.includes('?') ? '&' : '?';
  const data = curl(`${API_BASE}${path}${separator}limit=${limit}`);
  // Some endpoints return data directly as an array, others wrap in { items: [] }
  if (Array.isArray(data)) return data;
  return data.items || [];
}

// Fetch all locales for endpoints that filter by locale (FAQ, Legal)
async function fetchAllLocales(path) {
  const locales = ['en', 'fr', 'vi'];
  const results = [];
  for (const loc of locales) {
    try {
      const separator = path.includes('?') ? '&' : '?';
      const url = `${API_BASE}${path}${separator}locale=${loc}&limit=200`;
      const data = curl(url);
      const items = Array.isArray(data) ? data : (data.items || []);
      results.push(...items);
    } catch (err) {
      console.error(`  Failed to fetch ${path} for locale ${loc}:`, err.message);
    }
  }
  return results;
}

async function generateProducts() {
  console.log('[codegen] Generating products via API...');

  const items = await fetchAll('/api/v1/products?published=true&locale=en');
  const products = [];

  for (const p of items) {
    try {
      const detail = curl(`${API_BASE}/api/v1/products/${p.id}`);
      products.push(detail);
    } catch (err) {
      console.error(`  Failed to fetch product ${p.slug}:`, err.message);
    }
  }

  if (products.length === 0) {
    console.log('[codegen] No published products found');
    return;
  }

  const lines = [
    '// Auto-generated by CMS codegen. DO NOT EDIT MANUALLY.',
    `// Generated: ${new Date().toISOString()}`,
    '// Total published products: ' + products.length,
    '',
    'const cmsProducts = [',
  ];

  for (const p of products) {
    const images = (p.product_images || []).sort((a, b) => a.sort_order - b.sort_order);
    const en = (p.product_translations || []).find((t) => t.locale === 'en');
    const specs = p.product_specs || {};

    const imgObjects = images
      .map((img) => {
        const v = img.media?.variants || {};
        if (v.processed && v.variants) {
          const variantEntries = Object.entries(v.variants);
          const srcset = variantEntries
            .sort((a, b) => a[1].width - b[1].width)
            .map(([, d]) => `${d.url} ${d.width}w`)
            .join(', ');
          return {
            src: v.publicUrl,
            w: v.originalWidth || 1600,
            h: v.originalHeight || 1600,
            srcset,
          };
        }
        return v.publicUrl || '';
      })
      .filter((img) => img !== '');

    lines.push('  {');
    lines.push(`    slug: '${p.slug}',`);
    lines.push(`    name: '${(en?.name || '').replace(/'/g, "\\'")}',`);
    lines.push(`    category: '${CMS_CATEGORY_MAP[p.category] || p.category}',`);
    lines.push(`    image: ${JSON.stringify(imgObjects)},`);
    lines.push(`    features: ${JSON.stringify(en?.features || [])},`);
    lines.push(`    benefits: ${JSON.stringify(en?.benefits || [])},`);

    const specEntries = Object.entries(specs).filter(([k, v]) => k !== 'id' && k !== 'product_id' && v);
    if (specEntries.length > 0) {
      lines.push('    specs: {');
      for (const [k, v] of specEntries) {
        lines.push(`      ${k}: '${String(v).replace(/'/g, "\\'")}',`);
      }
      lines.push('    },');
    }

    lines.push(`    downloads: ${JSON.stringify(p.downloads || [])},`);
    lines.push(`    sortOrder: ${p.sort_order || 0},`);
    lines.push('  },');
  }

  lines.push('];');
  lines.push('');
  lines.push('export { cmsProducts };');

  const outPath = join(OUTPUT_DIR, 'features/products/data/products.generated.js');
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, lines.join('\n'));
  console.log(`[codegen] Wrote ${products.length} products to products.generated.js`);
}

async function generateNews() {
  console.log('[codegen] Generating news via API...');
  const articles = await fetchAll('/api/v1/news?published=true');

  for (const a of articles) {
    const frontmatter = [
      '---',
      `title: "${(a.title || '').replace(/"/g, '\\"')}"`,
      `description: "${(a.description || '').replace(/"/g, '\\"')}"`,
      `date: "${a.date || ''}"`,
      `keywords: ${JSON.stringify(a.keywords || [])}`,
      `locale: "${a.locale}"`,
      a.hero_image_url ? `heroImage: "${a.hero_image_url}"` : '',
      '---',
      '',
      a.body_markdown || '',
    ].filter(Boolean).join('\n');

    const outPath = join(process.cwd(), 'content/news', a.locale, `${a.slug}.md`);
    mkdirSync(dirname(outPath), { recursive: true });
    writeFileSync(outPath, frontmatter);
  }
  console.log(`[codegen] Wrote ${articles.length} news articles`);
}

async function generateFaq() {
  console.log('[codegen] Generating FAQ via API...');
  const sections = await fetchAllLocales('/api/v1/faq');

  if (sections.length === 0) {
    console.log('[codegen] No published FAQ sections found');
    return;
  }

  const overrides = {};
  for (const s of sections) {
    if (!overrides[s.locale]) overrides[s.locale] = {};
    overrides[s.locale][s.section_key] = {
      title: s.title,
      shortTitle: s.short_title,
      items: s.items,
    };
  }

  const lines = [
    '// Auto-generated by CMS codegen. DO NOT EDIT MANUALLY.',
    `// Generated: ${new Date().toISOString()}`,
    '',
    `const cmsFaqOverrides = ${JSON.stringify(overrides, null, 2)};`,
    '',
    'export { cmsFaqOverrides };',
  ];

  const outPath = join(OUTPUT_DIR, 'features/faq/data/faq.generated.js');
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, lines.join('\n'));
  console.log(`[codegen] Wrote ${sections.length} FAQ sections to faq.generated.js`);
}

async function generateLegal() {
  console.log('[codegen] Generating legal pages via API...');
  const pages = await fetchAllLocales('/api/v1/legal');

  if (pages.length === 0) {
    console.log('[codegen] No legal page translations found');
    return;
  }

  const overrides = {};
  for (const p of pages) {
    if (!overrides[p.page_type]) overrides[p.page_type] = {};
    overrides[p.page_type][p.locale] = {
      title: p.title,
      description: p.description,
      eyebrow: p.eyebrow,
      h1: p.h1,
      lead: p.lead,
      intro: p.intro,
      sections: p.sections,
    };
  }

  const lines = [
    '// Auto-generated by CMS codegen. DO NOT EDIT MANUALLY.',
    `// Generated: ${new Date().toISOString()}`,
    '',
    `const cmsLegalOverrides = ${JSON.stringify(overrides, null, 2)};`,
    '',
    'export { cmsLegalOverrides };',
  ];

  const outPath = join(OUTPUT_DIR, 'features/legal/data/legal.generated.js');
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, lines.join('\n'));
  console.log(`[codegen] Wrote ${pages.length} legal page translations to legal.generated.js`);
}

async function generateSeoKeywords() {
  console.log('[codegen] Generating SEO keywords via API...');
  const items = await fetchAll('/api/v1/site-settings');

  const seoSetting = items.find((s) => s.key === 'seo_defaults');
  const seoData = seoSetting?.value || {};
  const coreKeywords = seoData.coreKeywords || [];
  const secondaryKeywords = seoData.secondaryKeywords || [];

  const lines = [
    '// Auto-generated by CMS codegen. DO NOT EDIT MANUALLY.',
    `// Generated: ${new Date().toISOString()}`,
    '',
    `const cmsCoreKeywords = ${JSON.stringify(coreKeywords)};`,
    `const cmsSecondaryKeywords = ${JSON.stringify(secondaryKeywords)};`,
    '',
    'export { cmsCoreKeywords, cmsSecondaryKeywords };',
  ];

  const outPath = join(OUTPUT_DIR, 'shared/seo/seoKeywords.generated.js');
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, lines.join('\n'));
  console.log('[codegen] Wrote SEO keywords to seoKeywords.generated.js');
}

async function generateI18n() {
  console.log('[codegen] Generating i18n strings via API...');
  const siteSettings = await fetchAll('/api/v1/site-settings');

  const cmsMessages = { en: {}, fr: {}, vi: {} };

  for (const s of siteSettings) {
    const val = typeof s.value === 'string' ? s.value : s.value;
    if (s.key === 'site_name') {
      for (const loc of ['en', 'fr', 'vi']) {
        cmsMessages[loc].siteName = val;
      }
    }
    if (s.key === 'contact_info') {
      const contact = typeof val === 'object' ? val : {};
      for (const loc of ['en', 'fr', 'vi']) {
        cmsMessages[loc].contactInfo = contact;
      }
    }
  }

  const lines = [
    '// Auto-generated by CMS codegen. DO NOT EDIT MANUALLY.',
    `// Generated: ${new Date().toISOString()}`,
    '',
    `const cmsMessages = ${JSON.stringify(cmsMessages, null, 2)};`,
    '',
    'export { cmsMessages };',
  ];

  const outPath = join(OUTPUT_DIR, 'shared/lib/i18n.generated.js');
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, lines.join('\n'));
  console.log('[codegen] Wrote i18n strings to i18n.generated.js');
}

async function main() {
  console.log('[codegen] Starting content generation from API...');
  console.log(`[codegen] API: ${API_BASE}`);

  await generateProducts();
  await generateNews();
  await generateFaq();
  await generateLegal();
  await generateSeoKeywords();
  await generateI18n();

  console.log('[codegen] Done.');
}

main().catch((err) => {
  console.error('[codegen] Fatal error:', err);
  process.exit(1);
});
