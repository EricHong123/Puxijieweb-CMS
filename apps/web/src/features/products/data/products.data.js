export function buildSheetSpecs(product) {
  const specs = [
    ['Main function', product.functionSet],
    ['Material / craft', product.material],
    ['Chipset', product.chipset],
    ['Bluetooth version', product.bluetoothVersion],
    ['Transmission distance', product.transmissionDistance],
    ['Speaker', product.speakerSpec],
    ['Battery', product.batterySpec],
    ['Playback time', product.batteryLife],
    ['Product size', product.dimensions],
    ['Net weight', product.weight],
    ['Color options', product.colorOptions],
  ];

  if (product.category === 'Bluetooth Earbuds') {
    return [
      ['Model', product.name],
      ['Chipset', product.chipset],
      ['Bluetooth version', product.bluetoothVersion],
      ['Frequency range', product.frequencyRange],
      ['Working range', product.transmissionDistance],
      ['Earbud battery', product.earbudBattery],
      ['Charging case battery', product.caseBattery],
      ['Color options', product.colorOptions],
    ].filter(([, value]) => value);
  }

  return specs.filter(([, value]) => value);
}

